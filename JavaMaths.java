public class JavaMathsEg
{
public static void main(String args[])
{
double n=25;
double m=225;

//return the maximum of two numbers
System.out.println("Maximum number of m and n is:"+Math.max(n,m));

//return the square root of y
System.out.println("Square root of  m is:"+Math.sqrt(m));

//returns 28 to the power 4 as 28*28*28*28
System.out.println("Power of n and m is:"+Math.pow(n,m));

//return logaruthm of given value
System.out.println("Logarithm of n is:"+Math.log(n));
System.out.println("Logarithm of m is:"+Math.log(m));

//return the logarithm of given value when base is 10
System.out.println("Log10 of n is:"+Math.log10(n));
System.out.println("Log10 of m is:"+Math.log10(m));

//return a power of 2
System.out.println("exp of a is :"+Math.exp(n));

//return (a power of 2)-1
System.out.println("expm1 of a is:"+Math.expm1(n));

}
}
